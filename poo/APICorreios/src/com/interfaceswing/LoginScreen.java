    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        String username = jTextFieldUsername.getText();
        String password = jPasswordField.getText();
        
        try{
            java.sql.Connection connection = MySQLConnection.getMySQLConnection();
             // Consulta SQL para verificar as credenciais
            String sql = "SELECT * FROM tbl_users WHERE username = ? AND pass = ?";
            try (PreparedStatement statement = (PreparedStatement) connection.prepareStatement(sql)) {
                statement.setString(1, username);
                statement.setString(2, password);

                try (ResultSet resultSet = statement.executeQuery()) {
                    if (resultSet.next()) {
                        // Credenciais corretas, chame a tela HomeScreen
                        HomeScreen homeScreen = new HomeScreen(username);
                        homeScreen.setVisible(true);
                        this.dispose(); // Feche a tela de login se as credenciais estiverem corretas
                    } else {
                        // Credenciais incorretas, exiba uma mensagem de erro
                        jLabelError.setText("Usu√°rio e/ou senha incorretos.");
                    }
                }
            }
            
        } catch(SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

